                          <h1 style="text-align: center; padding-top: 50px;">《网络交易安全协议》</h1>
    <div class="prod">
        <p>1.提高工作人员的维护网络安全的警惕性和自觉性。</p>
        <p>2. 负责对本网络用户进行安全教育和培训，使用户自觉遵守和维护《计算机信息网络国际互联网安全保护管理办法》，使他们具备基本的网络安全知识。</p>
        <p> 3. 加强对单位的信息发布和BBS公告系统的信息发布的审核管理工作，杜绝违犯《计算机信息网络国际互联网安全保护管理办法》的内容出现。</p>
        <p> 4. 一旦发现从事下列危害计算机信息网络安全的活动的：</p>
        <p>    （一）未经允许进入计算机信息网络或者使用计算机信息网络资源；</p>
        <p>    （二）未经允许对计算机信息网络功能进行删除、修改或者增加；</p>
        <p>    （三）未经允许对计算机信息网络中存储、处理或者传输的数据和应用程序进行删除、 修改或者增加；</p>
        <p>   （四）故意制作、传播计算机病毒等破坏性程序的；</p>
        <p>   （五）从事其他危害计算机信息网络安全的活动。做好记录并立即向当地公安机关报告。</p>
        <p> 5. 在信息发布的审核过程中，如发现有以下行为的：</p>
        <p>   （一）煽动抗拒、破坏宪法和法律、行政法规实施</p>
        <p>  （二）煽动颠覆国家政权，推翻社会主义制度</p>
        <p>   （三）煽动分裂国家、破坏国家统一</p>
        <p>   （四）煽动民族仇恨、民族歧视、破坏民族团结</p>
        <p>  （五）捏造或者歪曲事实、散布谣言，扰乱社会秩序</p>
        <p>  （六）宣扬封建迷信、淫秽、色情、赌博、暴力、凶杀、恐怖、教唆犯罪</p>
        <p>  （七）公然侮辱他人或者捏造事实诽谤他人</p>
        <p>  （八）损害国家机关信誉</p>
        <p>  （九）其他违反宪法和法律、行政法规将一律不予以发布，并保留有关原始记录，在二十四小时内向当地公安机关报告。</p>
        <p> 6. 接受并配合公安机关的安全监督、检查和指导，如实向公安机关提供有关安全保护的信息、资料及数据文件，协助公安机关查处通过国际联网的计算机信息网络的违法犯罪行为. 信息发布登记制度。 </p>
        <p>   1. 在信源接入时要落实安全保护技术措施，保障本网络的运行安全和信息安全；</p>
        <p>  2. 对以虚拟主机方式接入的单位，系统要做好用户权限设定工作，不能开放 其信息目录以外的其他目录的操作权限。</p>
        <p>  3. 对委托发布信息的单位和个人进行登记并存档。</p>
        <p> 4. 对信源单位提供的信息进行审核，不得有违犯《计算机信息网络国际联网安全保护管理办法》的内容出现。</p>
        <p> 5. 发现有违犯《计算机信息网络国际联网安全保护管理办法》情形的，应当保留有关原始记录，并在二十四小时内向当地公安机关报告。</p>
        <p>信息内容审核制度：</p>
        <p>一、必须认真执行信息发布审核管理工作，杜绝违犯《计算机信息网络国际联网安全保护 管理办法》的情形出现。</p>
        <p>二、对在本网站发布信息的信源单位提供的信息进行认真检查，不得有危害国家安全、泄露国家秘密，侵犯国家的、社会的、集体的利益和公民的合法权益的内容出现。</p>
        <p> 三、对在BBS公告板等发布公共言论的栏目建立完善的审核检查制度，并定时检查，防止违犯《计算机信息网络国际联网安全保护管理办法》的言论出现。</p>
        <p> 四、一旦在本信息港发现用户制作、复制、查阅和传播下列信息的：</p>
        <p>  1. 煽动抗拒、破坏宪法和法律、行政法规实施</p>
        <p>  2. 煽动颠覆国家政权，推翻社会主义制度</p>
        <p> 3. 煽动分裂国家、破坏国家统一</p>
        <p> 4. 煽动民族仇恨、民族歧视、破坏民族团结</p>
        <p> 5. 捏造或者歪曲事实、散布谣言，扰乱社会秩序</p>
        <p> 6. 宣扬封建迷信、淫秽、色情、赌博、暴力、凶杀、恐怖、教唆犯罪</p>
        <p>  7. 公然侮辱他人或者捏造事实诽谤他人</p>
        <p> 8. 损害国家机关信誉</p>
        <p> 9. 其他违反宪法和法律、行政法规</p>
        <p> 10. 按照国家有关规定，删除本网络中含有上述内容的地址、目录或者关闭服务器。并保留原始记录，在二十四小时之内向当地公安机关报告。</p>
        <p>用户备案制度：</p>
        <p> 一、用户在本单位办理入网手续时，应当填写用户备案表</p>
        <p> 二、公司设专人按照公安部《中华人民共和国计算机信息网络国际联网单位备案表的通知》的要求，在每月20日前，将济南地区本月因特网及公众多媒体通信网（网外有权部分）新增、撤消用户的档案材料完整录入微机，并打印两份。</p>
        <p> 三、将本月新增、撤消的用户进行分类统计，并更改微机存档资料，同时打印一份。</p>
        <p> 四、每月20日之前，将打印出的网络用户的备案资料（2份）及统计信息（1份）送至市公安局专人处。</p>
        <p>安全教育培训制度 ：</p>
        <p> 一、定期组织管理员认真学习《计算机信息网络国际互联网安全保护管理办法》、《网络安 全管理制度》及《信息审核管理制度》，提高工作人员的维护网络安全的警惕性和自觉 性。</p>
        <p> 二、负责对本网络用户进行安全教育和培训，使用户自觉遵守和维护《计算机信息网络国际互联网安全保护管理办法》，使他们具备基本的网络安全知识。</p>
        <p> 三、对信息源接入单位进行安全教育和培训，使他们自觉遵守和维护《计算机信息网络国际 互联网安全保护管理办法》，杜绝发布违犯《计算机信息网络国际互<p>联网安全保护管理办法》的信息内容。</p>
        <p>四、不定期地邀请公安机关有关人员进行信息安全方面的培训，加强对有害信息，特别是影射性有害信息的识别能力，提高防犯能力。  电子公告系统的用户登记和信息管理制度</p>
        <p> 1. 建立健全计算机信息网络电子公告系统的用户登记和信息管理制度；</p>
        <p>  2. 组织网络管理员学习《计算机信息网络国际联网安全保护管理办法》，提高网络安全员的警惕性。</p>
        <p>  3. 负责对本网络用户进行安全教育和培训，</p>
        <p>  4. 建立电子公告系统的网络安全管理制度和考核制度，加强对电子公告系统的审核管理工作，杜绝BBS上出现违犯《计算机信息网络国际联网安全保护管理办法》的内容。</p>
        <p>    1. 对版主的聘用本着认真慎重的态度、认真核实版主身份，做好版主聘用记录。</p>
        <p>      2. 对各版聘用版主实行有针对性的网络安全教育，落实版主职责，提高版主的责任感。</p>
        <p>      3. 版主负责检查各版信息内容，如发现违反《计算机信息网络国际互联网安全保护管理办法》即时予以删除，情节严重者，做好原始记录，报告网络管理员解决，由管理员向公安机关计算机管理监察机构报告。</p>
        <p>      4. 网络管理员负责考核各版版主，如发现不能正常履行版主职责者，将予以警告，严重者予以解聘。</p>
        <p>      5. 在版主负责栏目中发现重大问题未得到及时解决者，即时对版主予以解聘。</p>
        <p>     6. 加强网络管理员职责，配合各版版主的工作，共同维护电子公告板的信息安全。</p>
        <p>         1. 检查时严格按照《计算机信息网络国际互联网安全保护管理办法》、《网络安全管理制度》及《信息审核管理制度》（见附页）的标准执行。</p>
        <p>         2. 如发现违犯《计算机信息网络国际互联网安全保护管理办法》（见附页）的言论及信息，即时予以删除，情节严重者保留有关原始记录，并在二十 四小时内向当地公安机关报告。</p>
        <p>         3. 负责对本网络用户进行安全教育和培训，网络管理员加强对《计算机信息网络国际互联网安全保护管理办法》的学习，进一步提高对网络信息安维护的警惕性。</p>
